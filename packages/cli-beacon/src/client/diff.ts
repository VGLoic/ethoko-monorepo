import { createHash } from "node:crypto";
import { CommandLogger } from "../ui";
import { PulledArtifactStore } from "../pulled-artifact-store/pulled-artifact-store";
import { toAsyncResult, toResult } from "../utils/result";
import { CliError } from "./error";

import { retrieveOrPullArtifact } from "./retrieve-or-pull-artifact";
import { StorageProvider } from "@/storage-provider";
import { ArtifactKey } from "@/utils/artifact-key";
import { EthokoContractOutputArtifact } from "@/ethoko-artifacts/v0";

/**
 * This script generates the differences between the given artifact and an already pushed artifact.
 *
 * For each artifact, the script will work on the differences on the contracts object.
 * This object contains as keys the path of a contract file and as values the contracts within it, i.e.
 * ```
 * {
 *  "output": {
 *   "contracts": {
 *      "path/to/foo.sol": {
 *         "Foo": {
 *             "abi": [...],
 *             "devdoc": {...},
 *             "evm": {
 *                "bytecode": {...},
 *                "deployedBytecode": {...},
 *                ...
 *             },
 *             "metadata": "...",
 *             "storageLayout": {...},
 *             "userdoc": {...}
 *         }
 *      },
 *      "path/to/bar.sol": {
 *         "Bar1": {
 *             "abi": [...],
 *             "devdoc": {...},
 *             "evm": {
 *                "bytecode": {...},
 *                "deployedBytecode": {...},
 *                ...
 *             },
 *             "metadata": "...",
 *             "storageLayout": {...},
 *             "userdoc": {...}
 *         },
 *         "Bar2": {
 *             "abi": [...],
 *             "devdoc": {...},
 *             "evm": {
 *                "bytecode": {...},
 *                "deployedBytecode": {...},
 *                ...
 *             },
 *             "metadata": "...",
 *             "storageLayout": {...},
 *             "userdoc": {...}
 *         },
 *      },
 *   }
 * }
 * ```
 *
 * For each contract, a hash is computed based on
 * - stringified abi,
 * - bytecode object,
 * - metadata
 * This hash is stored in a map with the `<file path><separator constant><contract name>` as key.
 *
 * Comparing the two maps, the script will output the differences between the two sets of contracts.
 */
export type Difference = {
  path: string;
  name: string;
  status: "added" | "removed" | "changed";
};

/**
 * Generates the differences between the artifacts generated by a fresh compilation and the ones of a pulled artifact.
 * It consists of the following steps:
 * 1. Check if the target artifact exists locally, if not throw an error
 * 2. Read the build info file of the fresh compilation artifact and generate the contract hashes map
 * 3. Read the build info file of the target artifact and generate the contract hashes map
 * 4. Compare the two maps and generate the differences array
 *
 * @throws CliError if there is an error during the execution of the steps, with user-friendly messages that can be directly shown to the user
 * @param artifactKey The key to the target artifact, containing the project and the tag or id of the target artifact
 * @param artifact The fresh compilation artifact to compare with the target artifact
 * @param storageProvider The StorageProvider instance to use for retrieving the target artifact if it's not available locally
 * @param pulledArtifactStore The store used to persist pulled artifacts
 * @param opts Options for the diff command
 * @param opts.debug Enable debug mode for more verbose logging
 * @param opts.logger The CommandLogger instance to use for logging and prompting the user during the diff process
 * @param opts.isCI Whether running in CI environment (disables interactive prompts)
 * @returns The array of differences between the fresh compilation artifact and the target artifact
 */
export async function generateDiffWithTargetRelease(
  artifactKey: ArtifactKey,
  artifact: {
    outputContractArtifacts: EthokoContractOutputArtifact[];
  },
  storageProvider: StorageProvider,
  pulledArtifactStore: PulledArtifactStore,
  opts: { debug: boolean; logger: CommandLogger; isCI?: boolean },
): Promise<Difference[]> {
  // Step 1: Check if target artifact exists locally

  const spinner1 = opts.logger.createSpinner(
    "Checking artifact existence locally...",
  );
  const ensureResult = await toAsyncResult(
    pulledArtifactStore.ensureProjectSetup(artifactKey.project),
    { debug: opts.debug },
  );
  if (!ensureResult.success) {
    spinner1.fail("Failed to setup pulled artifact store");
    throw new CliError(
      "Error setting up pulled artifact store, is the script not allowed to write to the filesystem? Run with debug mode for more info",
    );
  }

  const artifactId = await retrieveOrPullArtifact(
    artifactKey,
    storageProvider,
    pulledArtifactStore,
    { debug: opts.debug, logger: opts.logger },
  ).catch((err) => {
    spinner1.fail("Failed to retrieve or pull the target artifact");
    throw err;
  });
  spinner1.succeed("Artifact found");

  const virtualReleaseContractHashesResult = toResult(
    () => generateContractHashes(artifact.outputContractArtifacts),
    { debug: opts.debug },
  );
  if (!virtualReleaseContractHashesResult.success) {
    throw new CliError(
      "Error generating contract hashes from the fresh compilation artifact. Run with debug mode for more info",
    );
  }

  const spinner4 = opts.logger.createSpinner("Reading target artifact...");
  const contractListResult = await toAsyncResult(
    pulledArtifactStore.listContractArtifacts(artifactKey.project, artifactId),
    { debug: opts.debug },
  );
  if (!contractListResult.success) {
    spinner4.fail("Failed to read target artifact");
    throw new CliError(
      "Unable to retrieve the target artifact content, please ensure it exists locally. Run with debug mode for more info",
    );
  }

  const commonContracts = contractListResult.value.filter((contract) =>
    virtualReleaseContractHashesResult.value.has(
      formKey(contract.sourceName, contract.contractName),
    ),
  );

  const commonContractArtifactsResult = await toAsyncResult(
    Promise.all(
      commonContracts.map((contract) =>
        pulledArtifactStore.retrieveContractOutputArtifact(
          artifactKey.project,
          artifactId,
          contract.sourceName,
          contract.contractName,
        ),
      ),
    ),
    { debug: opts.debug },
  );
  if (!commonContractArtifactsResult.success) {
    spinner4.fail("Failed to read target artifact");
    throw new CliError(
      "Unable to retrieve the target artifact content, please ensure it exists locally. Run with debug mode for more info",
    );
  }

  const targetReleaseContractHashesResult = toResult(
    () => generateContractHashes(commonContractArtifactsResult.value),
    { debug: opts.debug },
  );
  if (!targetReleaseContractHashesResult.success) {
    spinner4.fail("Failed to analyze target artifact");
    throw new CliError(
      "Error generating contract hashes for the target artifact. Run with debug mode for more info",
    );
  }
  spinner4.succeed("Target artifact loaded");

  const spinner5 = opts.logger.createSpinner("Computing differences...");
  const differences: Difference[] = [];
  for (const [
    contractKey,
    contractHash,
  ] of virtualReleaseContractHashesResult.value.entries()) {
    const { contractPath, contractName } = parseKey(contractKey);
    const targetReleaseHash =
      targetReleaseContractHashesResult.value.get(contractKey);
    if (!targetReleaseHash) {
      differences.push({
        path: contractPath,
        name: contractName,
        status: "added",
      });
    } else if (targetReleaseHash !== contractHash) {
      differences.push({
        path: contractPath,
        name: contractName,
        status: "changed",
      });
    }
  }

  for (const contractKey of targetReleaseContractHashesResult.value.keys()) {
    if (!virtualReleaseContractHashesResult.value.has(contractKey)) {
      const { contractPath, contractName } = parseKey(contractKey);
      differences.push({
        path: contractPath,
        name: contractName,
        status: "removed",
      });
    }
  }
  spinner5.succeed("Differences computed");

  return differences;
}

function generateContractHashes(
  contractArtifacts: EthokoContractOutputArtifact[],
): Map<string, string> {
  const contractHashes = new Map<string, string>();
  for (const contractArtifact of contractArtifacts) {
    const hash = hashContract(contractArtifact.output.contract);
    contractHashes.set(
      formKey(contractArtifact.sourceName, contractArtifact.contract),
      hash,
    );
  }

  return contractHashes;
}

type Contract = EthokoContractOutputArtifact["output"]["contract"];
function hashContract(contract: Contract): string {
  const hash = createHash("sha256");

  contract.abi.sort((a, b) => {
    if (a.type !== b.type) {
      return a.type.localeCompare(b.type);
    }
    if (a.type === "function" && b.type === "function") {
      return a.name.localeCompare(b.name);
    }
    if (a.type === "event" && b.type === "event") {
      return a.name.localeCompare(b.name);
    }
    if (a.type === "error" && b.type === "error") {
      return a.name.localeCompare(b.name);
    }
    if (a.type === "constructor" && b.type === "constructor") {
      return 0;
    }
    if (a.type === "fallback" && b.type === "fallback") {
      return 0;
    }
    if (a.type === "receive" && b.type === "receive") {
      return 0;
    }
    return 0;
  });
  for (const abiItem of contract.abi) {
    hash.update(JSON.stringify(abiItem));
  }

  hash.update(contract.evm.bytecode.object);
  hash.update(contract.metadata);

  return hash.digest("hex");
}

const SEPARATOR = "@@@@";
function formKey(contractPath: string, contractName: string): string {
  return `${contractPath}${SEPARATOR}${contractName}`;
}
function parseKey(key: string): { contractPath: string; contractName: string } {
  const [contractPath, contractName] = key.split(SEPARATOR);
  if (!contractPath || !contractName) {
    throw new Error(`Invalid key: ${key}`);
  }
  return { contractPath, contractName };
}
